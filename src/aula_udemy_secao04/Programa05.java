package aula_udemy_secao04;
//Instrução Switch
public class Programa05 {
 public static void main(String[] Args) {
	int numero = 1;
	

 
 switch (numero) {
case 1:
	System.out.println("O numero eh 1");	
	break;
case 2:
	System.out.println("O numero eh 2");
	break;
case 3:
	System.out.println("O numero eh 3");
	break;
default:
	System.out.println("O numero eh " + numero);
	break;
 				}
 	}
 
 
}